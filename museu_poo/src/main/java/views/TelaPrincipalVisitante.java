    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (reservaSelecionada != null) {
            int confirmacao = JOptionPane.showConfirmDialog(this, "Deseja cancelar a reserva " + reservaSelecionada.getIdReserva() + "?", "Confirmar", JOptionPane.YES_NO_OPTION);
            
            if (confirmacao == JOptionPane.YES_OPTION) {
                ReservaDAO dao = new ReservaDAO();
                dao.destroy(reservaSelecionada);
                limpar();
                preencherTabela();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma reserva na tabela para excluir.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(reservaSelecionada != null) {
             try {
                // 1. Pega Data e Hora
                reservaSelecionada.setDataReserva(Date.valueOf(txtData.getText()));
                reservaSelecionada.setHorario(Time.valueOf(txtHora.getText()));
                
                // 2. Pega a Atração do ComboBox
                if (cmbAtracao.getSelectedItem() != null) {
                    ReservaAtracao ra = new ReservaAtracao();
                    ra.setReserva(reservaSelecionada);
                    ra.setAtracao((Atracao) cmbAtracao.getSelectedItem());
                    ra.setHorarioVisita(reservaSelecionada.getHorario());
                    reservaSelecionada.getAtracoes().clear(); // Limpa lista antiga
                    reservaSelecionada.addAtracao(ra);        // Adiciona nova
                }

                // 3. Pega o Guia do ComboBox
                if (cmbGuia.getSelectedItem() != null) {
                    ReservaGuia rg = new ReservaGuia();
                    rg.setReserva(reservaSelecionada);
                    rg.setGuia((Guia) cmbGuia.getSelectedItem());
                    reservaSelecionada.getGuias().clear(); // Limpa lista antiga
                    reservaSelecionada.addGuia(rg);        // Adiciona novo
                }
                
                // 4. Manda pro DAO
                ReservaDAO dao = new ReservaDAO();
                if(dao.update(reservaSelecionada)) { // Usa o boolean pra saber se deu certo
                    limpar();
                    preencherTabela();
                }
                
             } catch (Exception e) {
                 JOptionPane.showMessageDialog(null, "Erro ao editar. Verifique os dados.");
                 e.printStackTrace();
             }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma reserva para editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void tblReservaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblReservaMouseClicked
        int linha = tblReserva.getSelectedRow();
        if (linha != -1) {
            // Pega dados da tabela
            int id = Integer.parseInt(tblReserva.getValueAt(linha, 0).toString());
            String nomeAtracaoTabela = tblReserva.getValueAt(linha, 1).toString(); // Coluna 1 = Atração
            String data = tblReserva.getValueAt(linha, 2).toString();
            String hora = tblReserva.getValueAt(linha, 3).toString();
            String nomeGuiaTabela = tblReserva.getValueAt(linha, 4).toString();    // Coluna 4 = Guia
            
            // Preenche Data e Hora
            txtData.setText(data);
            txtHora.setText(hora);
            
            // SELECIIONA A ATRAÇÃO NO COMBOBOX (Varre a lista até achar o nome igual)
            for (int i = 0; i < cmbAtracao.getItemCount(); i++) {
                Atracao a = (Atracao) cmbAtracao.getItemAt(i);
                if (a.getNomeAtracao().equals(nomeAtracaoTabela)) {
                    cmbAtracao.setSelectedIndex(i);
                    break;
                }
            }

            // SELECIONA O GUIA NO COMBOBOX
            for (int i = 0; i < cmbGuia.getItemCount(); i++) {
                Guia g = (Guia) cmbGuia.getItemAt(i);
                if (g.getNomeGuia().equals(nomeGuiaTabela)) {
                    cmbGuia.setSelectedIndex(i);
                    break;
                }
            }
            
            // Prepara objeto para edição
            reservaSelecionada = new Reserva();
            reservaSelecionada.setIdReserva(id);
            // Nota: Não precisamos setar o resto aqui pois vamos pegar dos campos na hora de salvar
            
            btnCadastrar.setEnabled(false);
            btnEditar.setEnabled(true);
        }
    }//GEN-LAST:event_tblReservaMouseClicked

