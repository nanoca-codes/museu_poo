    private void preencherCombos() {
        cmbAtracao.removeAllItems();
        cmbGuia.removeAllItems();

        AtracaoDAO daoAtracao = new AtracaoDAO();
        for (Atracao a : daoAtracao.read()) {
            cmbAtracao.addItem(a);
        }

        GuiaDAO daoGuia = new GuiaDAO();
        for (Guia g : daoGuia.read()) {
            cmbGuia.addItem(g);
        }
    }
    
    private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblReserva.getModel();
        
        // 1. Configura as colunas na ordem que você quer
        modelo.setColumnIdentifiers(new Object[]{"ID", "Atração", "Data", "Hora", "Guia"});
        
        modelo.setNumRows(0);
        ReservaDAO dao = new ReservaDAO();
        
        // 2. Busca as reservas
        for (Reserva r : dao.read()) {
            // Filtra pelo visitante logado
            if (visitanteLogado != null && r.getVisitante() != null && 
                r.getVisitante().getIdVisitiante() == visitanteLogado.getIdVisitiante()) {
                
                // 3. Busca os nomes usando os métodos novos que criamos no DAO
                String nomeAtracao = dao.buscarNomeAtracao(r.getIdReserva());
                String nomeGuia = dao.buscarNomeGuia(r.getIdReserva());
                
                // 4. Adiciona na linha
                modelo.addRow(new Object[]{
                    r.getIdReserva(),
                    nomeAtracao,       // Agora mostra o nome (ex: Dinossauros)
                    r.getDataReserva(),
                    r.getHorario(),
                    nomeGuia           // Agora mostra o nome (ex: Carlos)
                });
            }
        }
    }
    
