    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            
            Reserva r = new Reserva();
            r.setDataReserva(Date.valueOf(txtData.getText())); // aaaa-mm-dd
            r.setHorario(Time.valueOf(txtHora.getText()));     // hh:mm:ss
            r.setVisitante(visitanteLogado);

       
            if (cmbAtracao.getSelectedItem() != null) {
                ReservaAtracao ra = new ReservaAtracao();
                ra.setReserva(r);
                ra.setAtracao((Atracao) cmbAtracao.getSelectedItem());
                ra.setHorarioVisita(r.getHorario());
                r.addAtracao(ra);
            }

            
            if (cmbGuia.getSelectedItem() != null) {
                ReservaGuia rg = new ReservaGuia();
                rg.setReserva(r);
                rg.setGuia((Guia) cmbGuia.getSelectedItem());
                r.addGuia(rg);
            }

            
            ReservaDAO dao = new ReservaDAO();
            if (dao.criarReservaCompleta(r)) {
                limpar();
                preencherTabela();
            }

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, "Data ou Hora inválida! \nUse: aaaa-mm-dd e hh:mm:ss");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

