    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String email = txtEmail.getText();
        String senha = new String(jPasswordField1.getPassword());

        VisitanteDAO dao = new VisitanteDAO();
        Visitante visitanteLogado = dao.checkLogin(email, senha);

        if (visitanteLogado != null) {
            JOptionPane.showMessageDialog(null, "Bem-vindo, " + visitanteLogado.getNomeVisitante());
            
            this.dispose();
            
            if (visitanteLogado.getTipoUsuario().equalsIgnoreCase("ADMIN")) {
                new TelaPainelAdmin().setVisible(true);
                
            } else {
                new TelaPrincipalVisitante(visitanteLogado).setVisible(true);
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Acesso Negado! Email ou senha incorretos.");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

