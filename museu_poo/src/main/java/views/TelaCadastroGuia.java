    private void tblGuiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGuiaMouseClicked
        int linha = tblGuia.getSelectedRow();
        if (linha != -1) {
            idGuiaSelecionado = Integer.parseInt(tblGuia.getValueAt(linha, 0).toString());
            // Preenche os campos usando os nomes corretos
            txtNomeGuia.setText(tblGuia.getValueAt(linha, 1).toString());
            txtEspecial.setText(tblGuia.getValueAt(linha, 2).toString());
            txtEmailGuia.setText(tblGuia.getValueAt(linha, 3).toString());
            
            btnCadastrar.setEnabled(false);
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
        }
    }//GEN-LAST:event_tblGuiaMouseClicked

