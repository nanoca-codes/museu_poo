    private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblGuia.getModel();
        modelo.setNumRows(0);
        // Colunas batendo com o desenho
        modelo.setColumnIdentifiers(new Object[]{"ID", "Nome", "Especialidade", "Email"});

        GuiaDAO dao = new GuiaDAO();
        for (Guia g : dao.read()) {
            modelo.addRow(new Object[]{
                g.getIdGuia(),
                g.getNomeGuia(),
                g.getEspecialidade(),
                g.getEmailGuia()
            });
        }
    }
    
